package WhileDöngüsüİleÇalışmak;

public class Main {

	public static void main(String[] args) {
		// For
		for (int i = 2; i < 10; i += 2) {
			System.out.println(i);
		}
		System.out.println("For döngüsü bitti! ");
		int i = 2;
		// while
		while (i < 10) {
			System.out.println(i);
			i += 2;
		}
		System.out.println("While döngüsü bitti! ");
	}

}
